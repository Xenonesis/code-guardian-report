// Development Service Worker - Minimal passthrough version
// This SW is used in development to avoid precache issues with changing file hashes

const isDev = self.location.hostname === 'localhost' || self.location.hostname === '127.0.0.1';

// Skip waiting and claim clients immediately
self.addEventListener('install', (event) => {
  if (isDev) {
    console.log('[SW Dev] Installing development service worker');
  }
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  if (isDev) {
    console.log('[SW Dev] Activating and clearing old caches');
  }
  // Clear all caches on activate to prevent stale cache issues
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (isDev) {
            console.log('[SW Dev] Clearing cache:', cacheName);
          }
          return caches.delete(cacheName);
        })
      );
    }).then(() => {
      return self.clients.claim();
    })
  );
});

// In development, just pass through all requests (network-first with no caching)
self.addEventListener('fetch', (event) => {
  // Skip non-http requests
  if (!event.request.url.startsWith('http')) {
    return;
  }
  
  // In development, just fetch from network without caching
  if (isDev) {
    event.respondWith(
      fetch(event.request).catch((error) => {
        console.warn('[SW Dev] Fetch failed:', event.request.url, error);
        throw error;
      })
    );
    return;
  }
  
  // For production, the full SW will be generated by Serwist build
  event.respondWith(fetch(event.request));
});

// Handle messages from the app
self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    self.skipWaiting();
  }
  
  if (event.data && event.data.type === 'CLEAR_CACHE') {
    caches.keys().then((names) => {
      names.forEach((name) => caches.delete(name));
    });
  }
});

// Handle push notifications (still functional in dev)
self.addEventListener('push', (event) => {
  if (event.data) {
    const payload = event.data.json();
    const title = payload.title || 'Code Guardian';
    const options = {
      body: payload.body || 'New notification',
      icon: payload.icon || '/favicon-192x192.svg',
      badge: payload.badge || '/favicon-192x192.svg',
      data: payload.data,
      tag: payload.tag || 'code-guardian-notification',
    };
    event.waitUntil(self.registration.showNotification(title, options));
  }
});

// Handle notification click
self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  const urlToOpen = event.notification.data?.url || '/';
  event.waitUntil(
    self.clients.matchAll({ type: 'window' }).then(async (clientList) => {
      for (const client of clientList) {
        if (client.url === urlToOpen && 'focus' in client) {
          return client.focus();
        }
      }
      if (self.clients.openWindow) {
        return self.clients.openWindow(urlToOpen);
      }
    })
  );
});

if (isDev) {
  console.log('[SW Dev] Development service worker loaded - no precaching active');
}
